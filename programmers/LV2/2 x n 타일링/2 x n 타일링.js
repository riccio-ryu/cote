/*
1 -> 1
2 -> 2
3 -> 3          111, 1--, --1
4 -> 5
5 -> 8          11111, 111--, 11--1, 1--11, --111, 1----, --1--, ----1
6 -> 13         111111, 1111--, 111--1, 11--11, 1--111, --1111, 11----, 1--1--, 1----1, --11--, --1--1, ----11, ------
*/
function solution(n) {
    let arr = [0,1,2]
    for(let i = 3; i <= n; i++){
        // console.log(arr[i-2]%1000000007, arr[i-1]%1000000007)
        arr[i] = arr[i-2]%1000000007 + arr[i-1]%1000000007
    }
    return arr[n]%1000000007;
}
