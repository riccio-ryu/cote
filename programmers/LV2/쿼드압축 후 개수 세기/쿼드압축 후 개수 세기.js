function solution(arr) {
    return fn(arr);
}

const fn = (arr) => {
    let n = arr.length      // 개수
    const sum = arr.flat().reduce((a,c) => a + c,0)
    if(sum === 0) return [1,0]  // 합이 0
    if(sum === n*n) return [0,1]    // 합이 n*n 즉, 모두 1
    
    let cnt = [0,0]
    n = n / 2
    for(let i = 0; i < 2; i++){
        for(let j = 0; j < 2; j++){
            // console.log(arr.slice(i*n, i*n+n))
            const next = arr.slice(i*n, i*n+n).map(row => row.slice(j*n, j*n+n));
            // console.log(i,j,n,next)
            const num = fn(next);
            // console.log('num ' ,num)
            cnt[0] += num[0];
            cnt[1] += num[1];
        }
    }
    return cnt
    // console.log(n, sum, arr)
}
